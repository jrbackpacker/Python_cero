## Operadores ##

print(3 + 5)
print(10 - 2)
print(4 * 7)
print(8 / 2)

# Operador de módulo (%)

print(5 % 2)

# Operador de división entera (nos da el cociente sin decimales)

print(5 // 2)

# Operador de exponenciación (elevado a...)

print(2 ** 3)   

#sobre decir que todos los operadores se pueden concatenar

print(2 + 3 * 4 - 5 / 2)

#podemos sumar texto

print("Hola " + "mundo")

#podemos multiplicar texto

print("Hola " * 3)

# Operadores de comparación numéricos

print(2 > 3)
print(2 < 3)
print(2 == 3)
print(2 != 3)
print(2 >= 3)
print(2 <= 3)

# Operadores de comparación de texto (orden alfabético)

print("Hola" > "Adiós")
print("Hola" < "Adiós")
print("Hola" == "Adiós")
print("Hola" != "Adiós")
print("Hola" >= "Adiós")
print("Hola" <= "Adiós")

#Operadores de comparación de texto mas len (cuenta caracteres)

print(len("Hola") > len("Adiós"))
print(len("Hola") < len("Adiós"))
print(len("Hola") == len("Adiós"))
print(len("Hola") != len("Adiós"))
print(len("Hola") >= len("Adiós"))
print(len("Hola") <= len("Adiós"))

#Operadores lógicos completo

print(True and False)
print(True or False)
print(not True) 


