### condicionales ###

# Los condicionales permiten ejecutar diferentes bloques de código según ciertas condiciones.
# La estructura básica es: if, elif y else.

edad = 20

if edad < 18:
    print("Eres menor de edad.")
elif edad == 18:
    print("Tienes 18 años.")
else:
    print("Eres mayor de edad.")

# Ejemplo con múltiples condiciones
nota = 50

if nota >= 90:
    print("Sobresaliente")
elif nota >= 80:
    print("Notable")
elif nota >= 70:
    print("Bien")
elif nota >= 60:
    print("Suficiente")
else:
    print("Insuficiente")

